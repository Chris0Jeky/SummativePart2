package task2;

public class PGAInstructor {
    private String name;
    private String coachingLevel;

    public PGAInstructor(String name, String level) {
        this.name = name;
        this.coachingLevel = level;
    }

    public String getName() {
        return name;
    }

    public String getCoachingLevel() {
        return coachingLevel;
    }

}
