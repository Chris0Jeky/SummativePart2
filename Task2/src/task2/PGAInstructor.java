package task2;

class PGAInstructor {
    private final String name;
    private final int coachLevel;

    public PGAInstructor(String name, int coachLevel) {
        this.name = name;
        this.coachLevel = coachLevel;
    }

    public String getName() {
        return name;
    }

    public int getCoachLevel() {
        return coachLevel;
    }
}
