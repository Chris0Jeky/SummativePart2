package task2;

class PGAInstructor {
    private String name;
    private int coachLevel;

    public PGAInstructor(String name, int coachLevel) {
        this.name = name;
        this.coachLevel = coachLevel;
    }

    public String getName() {
        return name;
    }

    public int getCoachLevel() {
        return coachLevel;
    }
}
